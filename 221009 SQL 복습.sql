------------------------------------------------------------------------------
---------------------------------------------------------------------------------------------------		
	select * from employee										
 		where hire_date >= to_date('1990-01-01', 'YYYY-MM-DD')	and hire_date <= to_date('1999-12-31', 'YYYY-MM-DD')
---------------------------------------------------------------------------------------------------		
	select * from employee										
 		where hire_date
			 between 
				(  (to_date('1990-01-01', 'YYYY-MM-DD') 
				and 
				to_date('1999-12-31', 'YYYY-MM-DD')  )
---------------------------------------------------------------------------------------------------		
	select * from employee										
 		where hire_date
			 between 
				(  (to_date('19900101', 'YYYYMMDD') 
				and 
				to_date('19991231', 'YYYYMMDD')  )
---------------------------------------------------------------------------------------------------
	select * from employee	
  where to_char(hire_date, 'YYYY') between '1990' and '1999'				
---------------------------------------------------------------------------------------------------
