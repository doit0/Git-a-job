---------------------------------------------------------------------------------------------------		
	select * from employee	
	where dep_no in (10, 30)								
	and ( salary < 3000 )
	and  hire_date < to_date('1996-01-01', 'YYYY-MM-DD') )
---------------------------------------------------------------------------------------------------		
	select * from employee	
	where dep_no = any (10, 30)							
	and ( salary < 3000
	and  hire_date < to_date('1996-01-01', 'YYYY-MM-DD') )
